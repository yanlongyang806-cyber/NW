'use strict';

/////////////////////////////////////////////////////////////////////////////

function Action(id, parentCard, actionDef)
{
	this.id = id;
	this.parentCard = parentCard;
	this.actionDef = actionDef;

	this.cooldown = 0;

	this.canExecute = false;
	this.chosen = false;
	this.noTargets = false;
}

/////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////

module.exports = Action;

